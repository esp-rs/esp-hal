/// Generate a ToneValue slice from Song beats and tempo
macro_rules! song {
        ($tempo:expr, [$(($note:expr, $duration:expr)),*]) => {
            {
                const WHOLENOTE: u32 = (600_000 * 4) / $tempo;
            [
                $(
                    ToneValue { frequency: $note, duration: WHOLENOTE / $duration },
                )*
            ]
            }
        };
    }

